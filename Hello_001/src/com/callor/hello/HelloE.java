package com.callor.hello;

/*
 * main() method 를 선언하고
 * 정수형 숫자 30과 40을 변수에 저장한 후
 * 4칙연산 결과를 출력하시오
 */
public class HelloE {
	
	public static void main(String[] args) {
		
		int num = 40;
		int num2 = 30;
		
		int a = num + num2;
		int b = num - num2;
		int c = num * num2;
		int d = num / num2;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
	}
}
